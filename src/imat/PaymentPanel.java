    public void changePanel(String s) {
        setInfoOk(false);
        if(currentPanel.equals("visa")) {
            v.clearErrorArea();
        } else if(currentPanel.equals("faktura")) {
            f.clearErrorArea();
        }
        
        currentPanel = s;
        CardLayout c = (CardLayout)(cardPanel.getLayout());
        c.show(cardPanel, s);
    }
    
